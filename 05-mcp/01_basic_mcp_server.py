from mcp.server.fastmcp import FastMCP

# ==========================================================
# Step 1 : Create MCP Server
# ==========================================================

mcp = FastMCP("Demo Server")

# ==========================================================
# Step 2 : Tool
# ==========================================================

@mcp.tool()
def calculator(a: int, b: int) -> int:
    """
    Add two numbers.
    """
    return a + b

# ==========================================================
# Step 3 : Run Server
# ==========================================================

if __name__ == "__main__":
    mcp.run()