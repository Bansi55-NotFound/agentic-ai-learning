"""
============================================================
PROJECT 08 : LANGGRAPH

Example 5 : Conditional Edges

Goal:
Learn how LangGraph makes decisions.

============================================================
"""

from typing import TypedDict
from langgraph.graph import StateGraph, END

# ==========================================================
# Step 1 : State
# ==========================================================

class State(TypedDict):
    count: int

# ==========================================================
# Step 2 : Graph
# ==========================================================

graph = StateGraph(State)

# ==========================================================
# Step 3 : Node
# ==========================================================

def counter_node(state: State):

    print(f"Current Count : {state['count']}")

    state["count"] += 1

    return state

# ==========================================================
# Step 4 : Decision Function
# ==========================================================

def should_continue(state: State):

    if state["count"] < 3:
        return "counter"

    return END

# ==========================================================
# Step 5 : Register Node
# ==========================================================

graph.add_node("counter", counter_node)

# ==========================================================
# Step 6 : Entry Point
# ==========================================================

graph.set_entry_point("counter")

# ==========================================================
# Step 7 : Conditional Edge
# ==========================================================

graph.add_conditional_edges(
    "counter",
    should_continue
)

# ==========================================================
# Step 8 : Compile
# ==========================================================

app = graph.compile()

# ==========================================================
# Step 9 : Run
# ==========================================================

app.invoke(
    {
        "count": 1
    }
)